static UINT8 rise11_dc3(UINT32 b, int adr)
{
	int t;
	UINT8 db, nb;
	nb = NIBBLE(b, 19,14,13,6);

	t = 0;
	if( adr >= 0x8ac0 )
		t = 1;
	db = rise11_crypt_table3[t][nb];
	return db;
}

