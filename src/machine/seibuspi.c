void seibuspi_rise10_text_decrypt(unsigned char *rom)
{
	int i;
	for(i=0; i<0x10000; i++) {
		UINT8 r1,r2,r3,r4,r5,r6;
		UINT32 w;

		w = (rom[(i*3) + 0] << 16) | (rom[(i*3) + 1] << 8) | (rom[(i*3) +2]);

		r1 = rise10_dc1(w, i);
		r2 = rise10_dc2(w, i);
		r3 = rise10_dc3(w, i);
		r4 = rise10_dc4(w, i);
		r5 = rise10_dc5(w, i);
		r6 = rise10_dc6(w, i);


		rom[(i*3) + 0] = (r1 << 4) | r2;
		rom[(i*3) + 1] = (r3 << 4) | r4;
		rom[(i*3) + 2] = (r5 << 4) | r6;
	}
}

void seibuspi_rise10_bg_decrypt(unsigned char *rom, int size)
{
	int i,j;
	for(j=0; j<size; j+=0xc0000) {
		for(i=0; i<0x40000; i++) {
			UINT8 r1,r2,r3,r4,r5,r6;
			UINT32 w;

			w = (rom[j + (i*3) + 0] << 16) | (rom[j + (i*3) + 1] << 8) | (rom[j + (i*3) + 2]);

			r1 = rise10_dc1(w, i/4);
			r2 = rise10_dc2(w, i/4);
			r3 = rise10_dc3(w, i/4);
			r4 = rise10_dc4(w, i/4);
			r5 = rise10_dc5(w, i/4);
			r6 = rise10_dc6(w, i/4);

			rom[j + (i*3) + 0] = (r1 << 4) | r2;
			rom[j + (i*3) + 1] = (r3 << 4) | r4;
			rom[j + (i*3) + 2] = (r5 << 4) | r6;
		}
	}
}

