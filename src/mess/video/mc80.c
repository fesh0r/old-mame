/*****************************************************************************/
/*                            Implementation for MC80.2x                     */
/*****************************************************************************/

UINT8* mc8020_video_ram;


VIDEO_START( mc8020 )
{
}

VIDEO_UPDATE( mc8020 )
{
	int x,y,j,b;
  	UINT16 addr;
  	int xpos;
  
	for(y = 0; y < 8; y++ )
	{
		addr = y*32;
		xpos = 0;
		for(x = 0; x < 32; x++ )
		{
			UINT8 code = mc8020_video_ram[addr + x] & 0x3f;						
			for(j = 0; j < 8; j++ )
			{				
			  for(b = 0; b < 6; b++ )
			  {
		  		*BITMAP_ADDR16(bitmap, y*16+j, xpos+b ) = (prom[code*8 + j] >> (5-b)) & 1;
			  }			  			  
			}
			xpos += 6;
		}
	}
	return 0;
}

/*****************************************************************************/
/*                            Implementation for MC80.3x                     */
/*****************************************************************************/

UINT8 *mc8030_video_mem;

VIDEO_START( mc8030 )
{
	mc8030_video_mem = auto_alloc_array_clear(machine, UINT8, 16*1024);
}

VIDEO_UPDATE( mc8030 )
{
	UINT8 code;
	int y, x, b;
	
	int addr = 0;	
	for (y = 0; y < 256; y++)
	{
		int horpos = 0;
		for (x = 0; x < 64; x++)
		{			
			code = mc8030_video_mem[addr++];
			for (b = 0; b < 8; b++)
			{				
				*BITMAP_ADDR16(bitmap, 255-y, horpos++) =  (code >> b) & 0x01;
			}
		}
	}
	return 0;
}
